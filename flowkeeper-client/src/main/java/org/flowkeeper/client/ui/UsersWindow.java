    private void populateUsersList() {
        usersList.setModel(new DefaultListModel() {
            /**
			 * 
			 */
			private static final long serialVersionUID = -5458109980667744703L;
			@Override
            public int getSize() {
                return users.size();
            }
            @Override
            public Object getElementAt(int index) {
                UserType u = users.get(index);
                return u.getName() + " (" + u.getId() + ")";
            }
        });
    }

