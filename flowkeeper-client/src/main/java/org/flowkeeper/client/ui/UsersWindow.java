    @Action
    public void leaveMessage() throws InvalidStateException {
        String msg = JOptionPane.showInputDialog(this, "Provide a text for this message", "Leave message", JOptionPane.PLAIN_MESSAGE);
        String userId = users.get(usersList.getSelectedIndex()).getId();
        server.sendMessage(userId, msg);
    }

